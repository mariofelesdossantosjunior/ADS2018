    private void calcularDesconto(Double valor, String cor) {
        double desconto = 0.0;
        
        switch (cor.toUpperCase()) {
            case "AZUL"://10%
                desconto = 10.0;
                break;
            case "VERDE"://20%
                desconto = 20.0;
                break;
            case "AMARELO"://30%
                desconto = 30.0;
                break;
            case "VERMELHO"://40%
                desconto = 40.0;
                break;
            case "LARANJA"://50%
                desconto = 50.0;
                break;
        }
        
        double total = valor - (valor * desconto / 100);
        System.out.println("O valor do produto com deconto Ã© : "+total);
    }
