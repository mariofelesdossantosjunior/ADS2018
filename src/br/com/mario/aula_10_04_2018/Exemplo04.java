package br.com.mario.aula_10_04_2018;

public class Exemplo04 {

    /**
     * Mostre os numeros do intervalo fechado de 10 a 30
     */
    public static void main(String[] args) {

        for (int cont = 10; cont <= 30; cont++) {
            System.out.println("Numero: " + cont);
        }
    }
}
