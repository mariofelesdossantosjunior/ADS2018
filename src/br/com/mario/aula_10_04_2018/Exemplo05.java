package br.com.mario.aula_10_04_2018;

public class Exemplo05 {

    /**
     * Mostre os numeros do intervalo fechado de 20 a 0
     */
    public static void main(String[] args) {

        for (int cont = 20; cont >= 0; cont--) {
            System.out.println("Numero: " + cont);
        }
    }
}
